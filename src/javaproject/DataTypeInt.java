package javaproject;

public class DataTypeInt {

    public static void main(String args[]) {
        int a = 50;
        int b = 20;
        int c = a + b;
        int d = a - b;
        int e = a * b;
        int f = a / b;
        System.out.println("Total is " + c);
        System.out.println("Total is " + d);
        System.out.println("Total is " + e);
        System.out.println("Total is " + f);
    }

}
