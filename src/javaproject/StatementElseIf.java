package javaproject;

public class StatementElseIf {

    public static void main(String args[]) {
        int marks = 15;
        if (marks == 5) {
            System.out.println("Marks is 5");
        } else if (marks == 10) {
            System.out.println("Marks is 10");
        } else if (marks == 15) {
            System.out.println("Marks is 15");
        } else {
            System.out.println("No match");
        }
    }

}
